    <h2>Introduction</h2>
    <section>
      <p>
        This research aims at understanding the fundamentals of Green Waste Management, its benefits and various solutions available for its deployment across domestic, agricultural and industrial setups.
      </p>
    </section>

    <h2>What is green waste?</h2>
    <section>
      <p>
        Green waste is a term given to waste matter like grass clippings, leaves, flowers, plant shreds, fruit and vegetable peels, food scraps etc. that are produced through cooking, gardening and irrigation.
      </p>
    </section>

    <h2>Green waste management</h2>
    <section>
      <p>
        It is essentially a system of collecting, segregating and processing of this organic biodegradable waste and turning it into a natural soil fertilizer called compost.
      </p>
    </section>

    <h2>What is compost?</h2>
    <section>
      <p>
        Compost is nothing but green waste left to decompose over a period of time.  It is regularly supplemented by water and exposure to air which enables the microorganisms in the content to break down the mixture into a hummus-like material that is nutrient rich.
      </p>
    </section>

    <h2>How does composting work?</h2>
    <section>
      <p>
        The microorganisms in the soil need four main ingredients (Carbon, Nitrogen, Oxygen and Water) to enable digestion and speed up the decomposition process.
      </p>
      <h3>Carbon</h3>
      <p>
        Energy in the form of heat is produced through microbial oxidation of carbon, if included at suggested levels. High carbon materials tend to be brown and dry.
      </p>
      <h3>Nitrogen</h3>
      <p>
        It is used to oxidize carbon and to reproduce more organisms. High nitrogen materials tend to be green or colorful and wet.
      </p>
      <h3>Oxygen</h3>
      <p>
        It is used for oxidizing carbon and the decomposition process.
      </p>
      <h3>Water</h3>
      <p>
        It is required in the right amount to maintain activity without causing anaerobic conditions.
      </p>
      <p>
        The resulting mixture contains high amounts of nitrogen among other nutrients which can increase the quality and efficiency of agricultural soil. It is used as a primary source of organic fertilizer for farming and gardening.
      </p>
    </section>

    <h2>Environmental benefits!</h2>
    <section>
      <h3>Soil Health</h3>
      <p>
        When mixed with soil before the farming cycle, compost produced from green waste has the property to replenish the soil with essential nutrients that act as organic fertilizers for the plants boosting their growth and quality.
        It eliminates the need for chemical fertilizers to a great extent further preventing the damage caused to the composition of the soil.
      </p>

      <h3>Prevents Global Warming</h3>
      <p>
        If green waste reaches landfills it decomposes anaerobically releasing methane gas which results in global warming due to the greenhouse effect.
        It also reduces carbon emissions resulting from burning waste in dump yards and collection pits.
      </p>

      <h3>Reduces Disposal Cost</h3>
      <p>
        It reduces the cost incurred from collection, transportation and dumping of waste materials.
      </p>

      <h3>Reduces Land Occupation</h3>
      <p>
        It reduces the area of land occupied in the landfills after being dumped along with other waste.
      </p>
