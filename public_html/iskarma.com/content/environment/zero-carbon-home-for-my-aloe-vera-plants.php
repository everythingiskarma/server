      <p>
        As I learn new things each day and become more aware, I find myself stripping away all my habits and practices that are detrimental to the environment.
      </p>
      <p>
        I was out looking for a pot for my new aloe vera plant. The cheapest ones were plastic containers that are certainly not environment friendly since plastic degrades and decomposes over 450 to a 1000 years. Which means, unless they are recycled after, they are going to occupy the landfills for that entire duration or burned away for energy, discharging poisonous gases into the atmosphere.
        I didn't buy!
      </p>
      <p>
        The next option was clay or ceramic pots, which are relatively better. But, I learned that their making process involves heating them up to 100° C to give them a hard form, which again, consumes wood in most workshops around my locality.
        I didn't buy!
      </p>
      <p>
        There are also cement or concrete pots that can be made without heating but the raw material itself has been heated up to 1450°C.
        I didn't buy!
      </p>
      <p>
        I returned empty handed and without a home for my plant. I resorted to YouTube and found several ideas for upcycling household waste items like oil cans, Amazon's packaging, plastic soda or water bottles etc. But I am not entirely satisfied by the mere lack of options and having to contrive ideas that are quite simply, hacks!
      </p>
      <p>
        While still in pursuit of a truly zero carbon pot for the plant, I am experimenting with the shell of a watermelon fruit which will soon begin to wither away!
      </p>
